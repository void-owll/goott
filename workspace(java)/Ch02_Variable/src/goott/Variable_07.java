package goott;

/*
 * 논리 자료형(boolean 형)
 * - 참이나 거짓을 판별(처리)하는 자료형.
 * - true = 참인 값.
 * - false = 거짓인 값.
 * - boolean 형의 기본 값은 false 임.
 * - True / False(X) ==> 반드시 소문자로 작성해야 함. 
 *   (python에서는 대문자형만을 인식했는데, 자바는 반대임)
 */
public class Variable_07 {

	public static void main(String[] args) {
		boolean test = false;
		
		test = true; 
		
		System.out.println("test >> " + test);
		
	}

}
